Color getAQIColor(int aqi) {
  if (aqi <= 50) return Colors.greenAccent;
  if (aqi <= 100) return Colors.yellowAccent;
  if (aqi <= 150) return Colors.orangeAccent;
  if (aqi <= 200) return Colors.redAccent;
  return Colors.purpleAccent;
}

String getAQIAdvice(int aqi) {
  if (aqi <= 50) return 'คุณภาพอากาศดี';
  if (aqi <= 100) return 'คุณภาพอากาศปานกลาง';
  if (aqi <= 150) return 'เริ่มมีผลกระทบต่อสุขภาพ';
  if (aqi <= 200) return 'คุณภาพอากาศไม่ดี';
  return 'อันตราย! ควรหลีกเลี่ยงกิจกรรมกลางแจ้ง';
}

